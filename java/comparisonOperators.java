public class comparisonOperators {
    public static void main(String[] args) {
        int a = 7;
        System.out.println(a > 4);
        System.out.println(a >= 7);
        System.out.println(a >= 8);
        System.out.println(a < 4);
        System.out.println(a <= 4);
        System.out.println(a <= 7);
        System.out.println(a == 4);
        System.out.println(a != 4);
    }
}
